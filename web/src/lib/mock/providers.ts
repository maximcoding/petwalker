import type { MockProvider, MockProviderService, ServiceCategory } from './types';

/* Fixture pool — realistic walker/sitter personas for the dog-walking
 * industry. Tone matches the brief: energetic, playful, warm. Names,
 * bios, and pricing reflect typical NYC-area market rates so the
 * Owner home looks credible during design QA.
 *
 * Photo paths point at /public/images/providers/<slug>.jpg — populated
 * by `pnpm photos:fetch` (Unsplash, free). If files are missing, the
 * `next/image` fallback renders the warm-200 placeholder.
 */

function provider(
  id: string,
  partial: Omit<MockProvider, 'id'>,
): MockProvider {
  return { id, ...partial };
}

const $ = (dollars: number) => Math.round(dollars * 100);

function svc(
  category: ServiceCategory,
  hourly: number,
  duration: number,
  extra: Partial<MockProviderService> = {},
): MockProviderService {
  return {
    category,
    hourlyRateCents: $(hourly),
    defaultDurationMin: duration,
    modes: ['timeSlot'],
    accommodation: ['atOwnerHome'],
    ...extra,
  };
}

